    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(validar()){
            String email = jTextField7.getText() +"@"+ jTextField8.getText();
            String tipo = "";
            if(jRadioButton1.isSelected()) tipo = "MINORISTA";
            else if (jRadioButton2.isSelected()) tipo = "MAYORISTA";
            Cliente cli = new Cliente(jTextField1.getText(),jTextField4.getText(),jTextField5.getText(),jTextField6.getText(),email,tipo);
            c.insertarCliente(cli);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed
    public boolean validar(){
        boolean valido = true;
        if(jTextField1.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese una cedula valido","Mensaje del sistema",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(jTextField4.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese un nombre valido","Mensaje del sistema",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(jTextField5.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese un apellido valido","Mensaje del sistema",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return valido;
    }
